<h1 class="text-6xl dark:text-white text-gray-900 text-center">About Emily.</h1>
<p class="text-xl dark:text-white text-gray-900 text-center">
    This project is a E-mail response generating website which connects to OpenAI's GPT-3 Large Language Model. 
    It allows users to simply paste the text of the email they wish to respond to in the text box and click the generate button.
    The website will then send the text to A custom API where it will generate a response which is then displayed on the page.
    The user can then copy the response and paste it into their email client or simply click the send button to send the email.(This feature is not yet implemented)
</p>